    private void tablapacienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablapacienteMouseClicked
        int fila = tablapaciente.rowAtPoint(evt.getPoint());
        if (evt.getClickCount() == 2) {
            seleccionar_esta_filapa(fila);
        }
    }//GEN-LAST:event_tablapacienteMouseClicked

    private void tablapacienteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablapacienteMousePressed

        int fila = tablapaciente.rowAtPoint(evt.getPoint());
        if (evt.getClickCount() == 2) {
            seleccionar_esta_filapa(fila);
        }

    }//GEN-LAST:event_tablapacienteMousePressed

    private void txtbuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarKeyReleased
        fpacientes func = new fpacientes();
        modelo = func.mostrar(txtbuscar.getText());
        tablapaciente.setModel(modelo);
        LOcultarColumna.ocultar_esta_columna(tablapaciente, 0);
    }//GEN-LAST:event_txtbuscarKeyReleased

    private void tablapacienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablapacienteKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int fila = tablapaciente.getSelectedRow();
            seleccionar_esta_filapa(fila);
        }
    }//GEN-LAST:event_tablapacienteKeyPressed

    private void txtbuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            tablapaciente.requestFocus();
            tablapaciente.changeSelection(0, 0, false, false);//sirve para selecionar una fila y una columna especifica
        }
    }//GEN-LAST:event_txtbuscarKeyPressed


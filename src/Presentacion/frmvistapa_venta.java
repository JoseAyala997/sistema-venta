    void seleccionar_esta_filapa(int fila) {

        if (dondebuscar == 1) {

            frmventas.txtcodcliente.setText(tablapaciente.getValueAt(fila, 0).toString());
            frmventas.txtcliente.setText(tablapaciente.getValueAt(fila,1).toString() + " " + tablapaciente.getValueAt(fila, 2).toString());
            frmventas.btnbProducto.requestFocus();
            this.dispose();
        }
        if (dondebuscar == 2) {

            frmcompras.lblidcliente.setText(tablapaciente.getValueAt(fila, 0).toString());
            frmcompras.txtcliente.setText(tablapaciente.getValueAt(fila, 1).toString() + " " + tablapaciente.getValueAt(fila, 2).toString());
            frmcompras.btnb_Cliente.requestFocus();
            this.dispose();
        }

    }

    public static void buscador() {

        if (dondebuscar == 1) {
            fpacientes func = new fpacientes();
            modelo = func.mostrar(txtbuscar.getText());
            tablapaciente.setModel(modelo);
            LOcultarColumna ocultar = new LOcultarColumna();
            ocultar.ocultar_esta_columna(tablapaciente, 0);
        }
        if (dondebuscar == 2) {
            fpacientes func = new fpacientes();
            modelo = func.mostrar(txtbuscar.getText());
            tablapaciente.setModel(modelo);
            LOcultarColumna ocultar = new LOcultarColumna();
            ocultar.ocultar_esta_columna(tablapaciente, 0);
        }

    }


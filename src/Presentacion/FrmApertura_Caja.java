    static void guardareditar() {
        if (txtNroCaja.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "FALTO EL NRO DE CAJA");
            return;
        }
        if (txtMontoApertura.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "FALTO INGRESAR EL MONTO DE APERTURA");
            return;
        }

        String montoApertura = txtMontoApertura.getText();
        String montoCierre = txtmontocierre.getText();

        try {

            montoApertura = montoApertura.replace(".", "");
            montoApertura = montoApertura.replace(",", ".");
            if (Long.parseLong(montoApertura) <= 0) {
                JOptionPane.showMessageDialog(null, "LA CANTIDAD NO PUEDE SER MENOR A 0");
                return;
            }

            montoCierre = montoCierre.replace(".", "");
            montoCierre = montoCierre.replace(",", ".");
            if (Long.parseLong(montoCierre) < 0) {
                JOptionPane.showMessageDialog(null, "EL COSTO NO PUEDE SER MENOR A 0");
                return;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "LA CANTIDAD INGRESADA DEL MONTO APERTURA O MONTO CIERRE ES INVALIDO");

            return;
        }

        fmovimiento_caja func = new fmovimiento_caja();

        if (!txtIDCaja.getText().equals("")) {
            opciones = "editar";
        } else {
            String[] registro = new String[9];
            registro = func.obtenerdatos(txtID.getText());
            if (!registro[0].equals("")) {
                JOptionPane.showMessageDialog(null, "ESTE USUARIO YA CUENTA CON UNA APERTURA DE CAJA");
                return;
            }

            opciones = "nuevo";
        }
        vmovimiento_caja dts = new vmovimiento_caja();

        dts.setNum_Caja(txtNroCaja.getText());
        dts.setMonto_apertura(Long.parseLong(montoApertura));
        dts.setMonto_cierre(Long.parseLong(montoCierre));
        Calendar cal;
        int d, m, a;
        cal = jFechaApertura.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        dts.setFecha_apertura(new Date(a, m, d));

        cal = jFechaCierre.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        dts.setFecha_cierre(new Date(a, m, d));

        dts.setEstado(cboestado.getSelectedItem().toString());
        dts.setIdusuarios(Integer.parseInt(txtID.getText()));

        if (opciones.equals("nuevo")) {
            if (func.insertar(dts)) {
                func.mostrar_ultimo_id(frmprincipal.lblcodusuario.getText());
                JOptionPane.showMessageDialog(null, "APERTURA CAJA EXITOSA CON : " + txtMontoApertura.getText() + " Gs.");
                inhabilitar();
                
            }
        } else {
            dts.setIdmovimiento(Integer.parseInt(txtIDCaja.getText()));
            if (func.editar(dts)) {
                func.mostrar_ultimo_id(frmprincipal.lblcodusuario.getText());
                JOptionPane.showMessageDialog(null, "DATOS EDITADOS CORRECTAMENTE");
                inhabilitar();
            }
        }
    }

